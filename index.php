  // Year
  document.getElementById('footerYear').textContent = new Date().getFullYear();

  // Show CTA button at md+
  const navCta = document.getElementById('navCta');
  const showCta = () => { navCta.style.display = window.innerWidth >= 640 ? 'inline-flex' : 'none'; };
  showCta(); window.addEventListener('resize', showCta);

  // Navbar scroll
  const navbar = document.getElementById('navbar');
  window.addEventListener('scroll', () => { navbar.classList.toggle('scrolled', window.scrollY > 24); }, { passive: true });

  // Mobile nav toggle
  const hamburger = document.getElementById('hamburger');
  const mobileNav = document.getElementById('mobileNav');
  const hIcon = document.getElementById('hIcon');
  const xIcon = document.getElementById('xIcon');

  hamburger.addEventListener('click', () => {
    const open = mobileNav.classList.toggle('open');
    hamburger.setAttribute('aria-expanded', String(open));
    mobileNav.setAttribute('aria-hidden', String(!open));
    hIcon.style.display = open ? 'none' : 'block';
    xIcon.style.display = open ? 'block' : 'none';
  });

  function closeNav() {
    mobileNav.classList.remove('open');
    hamburger.setAttribute('aria-expanded', 'false');
    mobileNav.setAttribute('aria-hidden', 'true');
    hIcon.style.display = 'block';
    xIcon.style.display = 'none';
  }

  // Scroll reveal
  const revealEls = document.querySelectorAll('.reveal');
  const ro = new IntersectionObserver((entries) => {
    entries.forEach(e => { if (e.isIntersecting) { e.target.classList.add('up'); ro.unobserve(e.target); } });
  }, { threshold: 0.08, rootMargin: '0px 0px -50px 0px' });
  revealEls.forEach(el => ro.observe(el));

  // Animated counters
  const counters = document.querySelectorAll('.stat-num[data-target]');
  const co = new IntersectionObserver((entries) => {
    entries.forEach(e => {
      if (!e.isIntersecting) return;
      const el = e.target;
      const target = +el.dataset.target;
      const suffix = el.dataset.suffix;
      const dur = 1800;
      const start = performance.now();
      const tick = (now) => {
        const p = Math.min((now - start) / dur, 1);
        const eased = 1 - Math.pow(1 - p, 3);
        el.textContent = Math.round(target * eased) + suffix;
        if (p < 1) requestAnimationFrame(tick);
      };
      requestAnimationFrame(tick);
      co.unobserve(el);
    });
  }, { threshold: 0.5 });
  counters.forEach(c => co.observe(c));
</script>
